package com.polus.fibicomp.view;

public class MobilePersonnelDetails {

	private String piFullName;

	private String email;

	private String leadUnit;

	private String certificationStatus;

	public String getPiFullName() {
		return piFullName;
	}

	public void setPiFullName(String piFullName) {
		this.piFullName = piFullName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getLeadUnit() {
		return leadUnit;
	}

	public void setLeadUnit(String leadUnit) {
		this.leadUnit = leadUnit;
	}

	public String getCertificationStatus() {
		return certificationStatus;
	}

	public void setCertificationStatus(String certificationStatus) {
		this.certificationStatus = certificationStatus;
	}
}
