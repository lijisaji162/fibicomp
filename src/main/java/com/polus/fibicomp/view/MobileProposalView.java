package com.polus.fibicomp.view;

public class MobileProposalView {

	private String proposalNo;

	private String title;

	private String documentNo;

	private String sponsor;

	private String pi;

	private String leadUnit;

	private String leadUnitNo;

	private String versionNo;

	private String status;

	private boolean certified;

	private String proposalPersonRoleId;

	private boolean certificationRequired;

	private String roleName;

	private String actionRequestCode;

	public String getProposalNo() {
		return proposalNo;
	}

	public void setProposalNo(String proposalNo) {
		this.proposalNo = proposalNo;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDocumentNo() {
		return documentNo;
	}

	public void setDocumentNo(String documentNo) {
		this.documentNo = documentNo;
	}

	public String getSponsor() {
		return sponsor;
	}

	public void setSponsor(String sponsor) {
		this.sponsor = sponsor;
	}

	public String getPi() {
		return pi;
	}

	public void setPi(String pi) {
		this.pi = pi;
	}

	public String getLeadUnit() {
		return leadUnit;
	}

	public void setLeadUnit(String leadUnit) {
		this.leadUnit = leadUnit;
	}

	public String getLeadUnitNo() {
		return leadUnitNo;
	}

	public void setLeadUnitNo(String leadUnitNo) {
		this.leadUnitNo = leadUnitNo;
	}

	public String getVersionNo() {
		return versionNo;
	}

	public void setVersionNo(String versionNo) {
		this.versionNo = versionNo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public boolean isCertified() {
		return certified;
	}

	public void setCertified(boolean certified) {
		this.certified = certified;
	}

	public String getProposalPersonRoleId() {
		return proposalPersonRoleId;
	}

	public void setProposalPersonRoleId(String proposalPersonRoleId) {
		this.proposalPersonRoleId = proposalPersonRoleId;
	}

	public boolean isCertificationRequired() {
		return certificationRequired;
	}

	public void setCertificationRequired(boolean certificationRequired) {
		this.certificationRequired = certificationRequired;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getActionRequestCode() {
		return actionRequestCode;
	}

	public void setActionRequestCode(String actionRequestCode) {
		this.actionRequestCode = actionRequestCode;
	}
}
