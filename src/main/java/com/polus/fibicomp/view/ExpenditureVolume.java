package com.polus.fibicomp.view;

public class ExpenditureVolume {

	private int budgetPeriod;

	private int directCost;

	private int fACost;

	public int getBudgetPeriod() {
		return budgetPeriod;
	}

	public void setBudgetPeriod(int budgetPeriod) {
		this.budgetPeriod = budgetPeriod;
	}

	public int getDirectCost() {
		return directCost;
	}

	public void setDirectCost(int directCost) {
		this.directCost = directCost;
	}

	public int getfACost() {
		return fACost;
	}

	public void setfACost(int fACost) {
		this.fACost = fACost;
	}

}
