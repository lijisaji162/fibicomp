package com.polus.fibicomp.view;

public class ExpenditureByAwardView {

	private Integer lineItemCost;

	private String costElement;

	private String description;

	public Integer getLineItemCost() {
		return lineItemCost;
	}

	public void setLineItemCost(Integer lineItemCost) {
		this.lineItemCost = lineItemCost;
	}

	public String getCostElement() {
		return costElement;
	}

	public void setCostElement(String costElement) {
		this.costElement = costElement;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
